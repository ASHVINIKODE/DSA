public class arrayTraversal {
    public static void main(String[] args) {
        int[] arr = { 2, 3, 4, 5, 6, 7 };
        System.out.println("Array elements are:");
        for (int i = 0; i < arr.length; i++) {
            System.out.println("Element at index " + i + "=" + arr[i]);
        }
    }
}